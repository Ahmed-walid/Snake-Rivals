	                          MOV  DX,CRITICAL_DIST
	                          SUB  AX,BX                                  	; AX WILL CONTAIN THE REAL DISTANCE
	                          CMP  AX,DX                                  	; COMPARE THE DISTANCE BETWEEN SNAKE1_HEAD_CENTER AND APPLE_X BY CRITICAL DISTANCE
	                          JBE  END_OF_SNAKE1_TRUE                     	; CONDITION IS TRUE

